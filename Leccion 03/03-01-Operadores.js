//Suma
let a = 3, b = 2;
let z = a + b;
console.log("Resultado de la suma: "+ z);

//Resta
z = a - b;
console.log("Resultado de la resta: "+ z);

//Multiplicacion
z = a * b;
console.log("Resultado de la multiplicacion: " + z);

//Division
z = a / b;
console.log("Resultado de la division: " + z );

//Residuo
z = a % b;
console.log("El residuo es: " + z);

//Exponente
z = a ** b;
console.log("Resultado de la potencia: " + z);

//INCREMENTO
//Pre-incremento ++ antes de la variable
z = ++a;
console.log(a);
console.log(z);
//Post-incremento ++ despues de la variable
z = b++;
console.log(b);
console.log(z);

//DECREMENTO
//Pre-decremento -- antes de la variable
z = --a;
console.log(a);
console.log(z);
//Post-decremento -- despues de la variable
z = b--;
console.log(b);
console.log(z);